// Arrow function.
// Compact way of writing a function.
// sum arrow function.
const Arrowsum = (a,b) => {
    return a + b;
}
console.log(Arrowsum(34,56));
// multiplication arrow function.
const Arrowmul = (x,y) => {
    return x * y;
}
console.log(Arrowmul(2,5));

