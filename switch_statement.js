// switch statement.
let mode = "dark grey";
switch(mode){
    case  "light":
        console.log("white");
        break;

    case "pink":
        console.log("pink");
        break;
    
    case "black":
        console.log("black");
        break;

    case "blue":
        console.log("blue");
        break;

    case "dark grey":
        console.log("Dark Grey");
        break;

    case "skylight":
        console.log("skyblue");
        break;
        
    default:
        console.log("Faild");
}