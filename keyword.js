// let, const & var
// 1.var keyword
var Fname = "Dipak Sirsath";
console.log(Fname);

var Fname = "Alpesh";
console.log(Fname);
 
// 2.let keyword
let fName = "Dipak";
console.log(fName);

fName = "ishawar";
console.log(fName);
fName = "yogesh";
console.log(fName);

// 3.const keyword
const num = 20;
console.log(num);

num = 22;
console.log(num);