// Comparison_operators

let a = 5;
let b = 2;
console.log("5 == 2 :-",a == b);
console.log("5 != 2 :-",a != b);
console.log("5 === 2 :-",a === b);
console.log("5 !== 2 :-", a !== b);

// >,>=,<,<=
console.log("5 > 2",a > b);
console.log("5 >= 2 :-",a >= 2);
console.log("5 < 2 :-",a < b);
console.log("5 <= 2 :-",a <= b);